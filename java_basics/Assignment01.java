package java_basics;

public class Assignment01 {
    public static void main(String[] args) {
        // Declaration and initialization of variables for each data type
        // byte
        byte ibyte = 127;
        System.out.println("Byte: " + ibyte);
        // short
        short ishort = 32767;
        System.out.println("Short: " + ishort);
        // int
        int iint = 2147483647;
        System.out.println("int: " + iint);
        // long
        long ilong = 9223372036854775807L;
        System.out.println("Long: " + ilong);
        // float
        float ifloat = 123.45f;
        System.out.println("Float: " + ifloat);
        // double
        double idouble = 12345.6789;
        System.out.println("Double: " + idouble);
        // char
        char ichar = 'U';
        System.out.println("Char: " + ichar);
        // Boolean
        boolean iboolean = true;
        System.out.println("Boolean: " + iboolean);
    }
}


