package java_basics;

public class Assignment05 {
	public static void main(String[]args) {
		 int number = 5;
		 System.out.println("Initial Value: " + number);
		 int incrementedValue = number++;
		 System.out.println("After post-increment (i++): " + incrementedValue);
		 int decrementedValue = number--;
		 System.out.println("After post-decrement (i--): " + decrementedValue);
	    }
	
	}

